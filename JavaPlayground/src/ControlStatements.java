
public class ControlStatements {
	
	public static void main(String[] args) {
		
//		------ if - else ------------------------
		
		int x=-90;
		
		if (x<0) {
			System.out.println("Negative Number");
		}
		else {
			System.out.println("Positive Number");
		}
		
//     ----------- Nested If Else ----------------------
//		a number is provided. if that number is divisible by 3  -- Fizz
//		is the same number is then divisible by 5 -- Buzz
//		if the same number is divisible by 15 print Fizz
//		Fizz-Buzz-Fizz
		
	}

}
